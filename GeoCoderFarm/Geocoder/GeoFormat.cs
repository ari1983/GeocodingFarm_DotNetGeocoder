namespace AM.Geocoding
{
    /// <summary>
    /// Specifies Geocoding Response Format
    /// </summary>
    public enum GeoFormat
    {
        /// <summary>
        /// Extensible Markup Language format
        /// </summary>
        XML,

        /// <summary>
        /// Java Script Object Notation format
        /// </summary>
        JSON,

        /// <summary>
        /// Geographic Really Simple Syndication format
        /// </summary>
        GeoRSS,

        /// <summary>
        /// Keyhole Markup Language format
        /// </summary>
        KML,

        /// <summary>
        /// GPS Exchange Format
        /// </summary>
        GPX
    }
}
